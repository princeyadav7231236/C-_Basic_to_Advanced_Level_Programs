#include<iostream>
using namespace std;

 int main(){ 
     int a, b;
     cout<<"Enter the values of a : ";
     cin>>a;
     cout<<"Enter the values of b : "; // This "<<" symbol is known as Insertion Operator.
     cin>>b;   // This ">>" symbol is known as Extraction Operator.
     cout<<"The SUM of a and b is : "<<a + b;
    
     return 0;
}
